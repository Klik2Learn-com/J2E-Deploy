 
Template.GGStressAudioSy1.events({

	'click .buttonaudio': function(evt) {
		$.k2l.GGStressAudioSy1.sound.src = $(evt.currentTarget).attr('data-audiosrc');
		addSpinner($.k2l.GGStressAudioSy1.sound,$(evt.currentTarget));
		$.k2l.GGStressAudioSy1.sound.play();
	},
	
	'click .pagination': function(evt) {
		
		$.k2l.GGStressAudioSy1.sound.src = {};
	}

});

Template.GGStressAudioSy1.rendered = function() {

	if(typeof $.k2l == 'undefined'){
		$.k2l = {};
	};
	
	if (typeof $.k2l.GGStressAudioSy1 == 'undefined') {
		$.k2l.GGStressAudioSy1 = {};
	};
	
	$.k2l.GGStressAudioSy1.sound = new Audio();
} 

